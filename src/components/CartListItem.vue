<template>
  <div class="product">
    <div class="product__image">
      <img :src="product.image" />
    </div>
    <div class="product__category">
      {{ product.category }}
    </div>
    <div class="product__name">
      {{ product.name }}
    </div>
    <div class="product__price">{{ product.price }} â‚½</div>
    <div class="product__changeCount">
      <div @click="decCount">-</div>
      {{ count }}
      <div @click="incCount">+</div>
    </div>
    <img
      class="product__remove"
      src="../assets/delete.png"
      @click="removeFromCart(product.id)"
    />
  </div>
</template>

<script>
import { mapActions } from "vuex";

export default {
  name: "CartListItem",
  props: ["product"],
  data: () => ({
    count: 0
  }),
  mounted() {
    this.count = this.product.count;
  },
  methods: {
    ...mapActions(["editCart", "removeFromCart"]),
    decCount() {
      if (this.count > 1) {
        this.count--;
        this.product.count = this.count;
        this.editCart(this.product);
      }
    },
    incCount() {
      if (this.count < 10) {
        this.count++;
        this.product.count = this.count;
        this.editCart(this.product);
      }
    },
    addProduct() {
      if (this.count) {
        this.product.count = this.count;
        this.addToCart(this.product);
        this.count = 0;
      }
    }
  }
};
</script>

